    def analyze_code(self, code: str) -> Dict:
        """تحليل كود وإعادة اقتراحات"""
        issues = []
        suggestions = []

        # Basic checks
        if "except:" in code and "pass" in code:
            issues.append("⚠️ تجنب except فارغ - استخدم معالجة الأخطاء المناسبة")

        if "print(" in code:
            suggestions.append("💡 استخدم logging بدلاً من print للإنتاج")

        if "TODO" in code or "FIXME" in code:
            suggestions.append("📝 هناك مهام غير مكتملة في الكود")

        if "import *" in code:
            issues.append("⚠️ تجنب import * - استخدم استيراد محدد")

        if len(code.split('\n')) > 100 and "def " not in code:
            suggestions.append("💡 فكر في تقسيم الكود إلى دوال")

        # Calculate complexity
        lines = code.split('\n')
        complexity = len([l for l in lines if l.strip() and not l.strip().startswith('#')])
        comment_ratio = len([l for l in lines if l.strip().startswith('#')]) / max(len(lines), 1)

        # Quality score
        quality_score = max(0, 100 - len(issues) * 15 + len(suggestions) * 5)
        quality_score = min(100, quality_score)

        return {
            "type": "code_analysis",
            "issues": issues,
            "suggestions": suggestions,
            "complexity": complexity,
            "lines": len(lines),
            "comment_ratio": round(comment_ratio, 2),
            "quality_score": quality_score,
            "response": f"📊 تم تحليل الكود: {len(lines)} سطر، درجة الجودة: {quality_score}/100",
            "confidence": 0.92
        }

